"""
AC 自动机参考 https://www.cnblogs.com/nullzx/p/7499397.html
感觉对性能有要求。应该是AC自动机得到各个串的出现位置，再用DFS找出能拼接的
方案1 失败，对于超时错误案例自测的时候超过递归最大深度
换用 BFS 也会超时  171/173
方案2 组合出所有的子串，然后做 AC 自动机输出结果
结果效果更差。。。 148/173
决定：对 171 特殊处理，单个字符的用方案2 ，其他情况用方案1（DFS）
"""

import sys
from typing import List

sys.setrecursionlimit(10000)


class Solution:

    class Node(object):
        def __init__(self, children=None, fail=None, value=""):
            self.children = children or {}
            self.fail = fail
            self.value = value

        def __str__(self):
            return f"Value: {self.value}\nChildren: {self.children}\nFail: {self.fail}"

    def build_trie_tree(self, words: List[str], root: Node):
        for word in words:
            curr = root
            for char in word:
                if char not in curr.children:
                    curr.children[char] = Solution.Node()
                curr = curr.children[char]
            curr.value = word

    def build_ac_from_trie_tree(self, root: Node):
        """ 在前缀树的基础上添加失败后的跳转节点 """
        queue = []
        for child in root.children.values():
            child.fail = root
            queue.append(child)
        while queue:
            first = queue.pop(0)
            for char, child in first.children.items():
                queue.append(child)
                fail_to = first.fail
                while True:
                    # 节点至少也是第二层的
                    if fail_to is None:  # 只有根节点的 fail 是 None
                        child.fail = root
                        break

                    if char in fail_to.children:
                        child.fail = fail_to.children[char]
                        break
                    fail_to = fail_to.fail

    def find_by_ac(self, text: str, root: Node):
        curr = root
        i = 0
        while i < len(text):
            char = text[i]
            if char in curr.children:
                curr = curr.children[char]
                if curr.value:
                    yield i - len(curr.value) + 1, curr.value
                if curr.fail and curr.fail.value:
                    yield i - len(curr.fail.value) + 1, curr.fail.value
                i += 1
            else:
                curr = curr.fail
                if curr is None:
                    curr = root
                    i += 1

    def find_words(self, word_map, start: int, word_count):
        if sum(word_count.values()) == 0:
            return True
        if start not in word_map:
            return False
        for word in word_map[start]:
            if word_count.get(word, 0) == 0:
                continue
            word_count[word] -= 1
            if self.find_words(word_map, start+len(word), word_count):
                word_count[word] += 1
                return True
            word_count[word] += 1
        return False

    def find_words_bfs(self, word_map, word_count):
        queue = []
        ret = []
        for start, words in word_map.items():
            for word in words:
                wc = {w: c for w, c in word_count.items()}
                wc[word] -= 1
                queue.append((start, start+len(word), wc))
        while queue:
            origin, start, wc = queue.pop(0)
            if sum(wc.values()) == 0:
                ret.append(origin)
                continue
            if start not in word_map:
                continue
            for word in word_map[start]:
                if wc.get(word, 0) == 0:
                    continue
                wc2 = {w: c for w, c in wc.items()}
                wc2[word] -= 1
                queue.append((origin, start+len(word), wc2))
        return ret

    def cal_word_count(self, words):
        ret = {}
        for word in words:
            ret[word] = ret.get(word, 0) + 1
        return ret

    def findSubstring(self, s: str, words: List[str]) -> List[int]:
        word_count = self.cal_word_count(words)
        if len(word_count) == 1:
            return self.findSubstring2(s, words)
        root = Solution.Node()
        self.build_trie_tree(words, root)
        self.build_ac_from_trie_tree(root)
        found = [r for r in self.find_by_ac(s, root)]
        found_map = {}
        for start, word in found:
            if start not in found_map:
                found_map[start] = []
            found_map[start].append(word)
        # DFS
        ret = []
        for start, word in found:
            if self.find_words(found_map, start, word_count):
                ret.append(start)

        # BFS
        # ret = self.find_words_bfs(found_map, word_count)
        return sorted(set(ret))

    def product(self, word_count):
        non_zero = [w for w, v in word_count.items() if v != 0]
        if len(non_zero) == 1:
            yield non_zero[0] * word_count[non_zero[0]]
            return
        for word, count in word_count.items():
            if count == 0:
                continue
            word_count[word] -= 1
            for r in self.product(word_count):
                yield word + r
            word_count[word] += 1

    def findSubstring2(self, s: str, words: List[str]) -> List[int]:
        word_count = self.cal_word_count(words)
        root = Solution.Node()
        self.build_trie_tree([s for s in self.product(word_count)], root)
        self.build_ac_from_trie_tree(root)
        return [r[0] for r in self.find_by_ac(s, root)]


